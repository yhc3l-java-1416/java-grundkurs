public class Demo2 {

	public static void main(String[] args) {

		for (int i = 0; i < 6; i++) {
			if (i == 1)
				System.out.println("i is one");
			else if (i == 2)
				System.out.println("i is two");
			else if (i == 3)
				System.out.println("i is three");
			else if (i == 4)
				System.out.println("i is four");
			else
				System.out.println("i is not between 1 - 4");

		}
	}

}
